//SENTENCIA SWITCH;

const dia = 23;

switch( dia ){
    case 0 :
        console.log("domingo");
        break;
    case 1 :
        console.log("lunes");
        break;
    case 2 :
        console.log("martes");
        break;
    default:
        console.log("no esta entre esos dias");
}


/* es importante colocar el break para romper con la 
secuencia de codigo cuando se cumpla una sentencia */
